#include <iostream>

/*
    + (addition)
    - (subtraction)
    * (moltiplication)
    / (division)
    % (modulus) means  "the rest of a division"
    ++ (increment)
    -- (decrement)
*/


int main(){

    int x = 10;

    // x = x + 1;
    x += 1;

    // x = x - 1;
    x -= 1;

    // x = x * 2;
    x *= 2;

    // x = x / 2;
    x /= 2;

    x = x % 2;
    x %= 2;


    return 0;
}